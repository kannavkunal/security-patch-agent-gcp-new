#!/usr/bin/env python3
"""
Simple GitHub Integration Test
Tests PR commenting directly with GitHub token from environment
"""
import os
import sys
from github import Github

def main():
    print("=" * 70)
    print("🧪 SIMPLE GITHUB PR COMMENT TEST")
    print("=" * 70)
    print()

    # Get token from environment or prompt
    token = os.getenv("GITHUB_TOKEN")
    if not token:
        print("⚠️  GITHUB_TOKEN environment variable not set")
        print()
        print("To run this test:")
        print("1. Get your GitHub token from: gcloud secrets versions access latest --secret=github-token")
        print("2. Run: export GITHUB_TOKEN='your-token-here'")
        print("3. Run this script again")
        print()
        return 1

    print("1️⃣ Authenticating with GitHub...")
    try:
        g = Github(token)
        user = g.get_user()
        print(f"   ✅ Authenticated as: {user.login}")
        print(f"   Name: {user.name}")
    except Exception as e:
        print(f"   ❌ Authentication failed: {e}")
        return 1

    print()
    print("2️⃣ Accessing repository...")
    try:
        repo = g.get_repo("kannavkunal/vulnerable-python-api")
        print(f"   ✅ Repository: {repo.full_name}")
        print(f"   Default branch: {repo.default_branch}")
    except Exception as e:
        print(f"   ❌ Cannot access repo: {e}")
        return 1

    print()
    print("3️⃣ Checking for test PR...")
    try:
        # Get or create a test PR
        pulls = list(repo.get_pulls(state='open'))[:5]

        if not pulls:
            print("   ⚠️  No open PRs found. Looking for closed PRs...")
            pulls = list(repo.get_pulls(state='closed', sort='created', direction='desc'))[:3]

        if not pulls:
            print("   ❌ No PRs found at all. Please create a test PR first.")
            return 1

        pr = pulls[0]
        print(f"   ✅ Using PR #{pr.number}: {pr.title}")
        print(f"   State: {pr.state}")
        print(f"   URL: {pr.html_url}")
    except Exception as e:
        print(f"   ❌ Error finding PR: {e}")
        return 1

    print()
    print("4️⃣ Creating security review comment...")
    comment_body = f"""## 🔒 Automated Security Review

**Scan ID:** `test-local-{os.getpid()}`

### Vulnerabilities Detected: 3

| Severity | Type | File | Line |
|----------|------|------|------|
| 🔴 CRITICAL | SQL Injection | app.py | 15 |
| 🟠 HIGH | Command Injection | app.py | 42 |
| 🟠 HIGH | Hardcoded Secret | app.py | 8 |

### Details

#### 1. SQL Injection (CRITICAL)
**File:** `app.py:15`
**Issue:** Unsafe SQL query construction using string formatting
**Code:**
```python
query = f'SELECT * FROM users WHERE id = {{user_id}}'
```
**Recommendation:** Use parameterized queries with placeholders

#### 2. Command Injection (HIGH)
**File:** `app.py:42`
**Issue:** Direct execution of user input
**Code:**
```python
exec(user_input)
```
**Recommendation:** Never use exec() with user input. Use safer alternatives.

#### 3. Hardcoded Secret (HIGH)
**File:** `app.py:8`
**Issue:** Database password hardcoded in source
**Code:**
```python
db_password = 'admin123'
```
**Recommendation:** Use environment variables or secret management

---

🤖 **This is a test comment from local development**
Generated by Security Patch Agent v1.0.0
"""

    try:
        comment = pr.create_issue_comment(comment_body)
        print(f"   ✅ Comment created successfully!")
        print(f"   Comment ID: {comment.id}")
        print(f"   Comment URL: {comment.html_url}")
    except Exception as e:
        print(f"   ❌ Failed to create comment: {e}")
        return 1

    print()
    print("5️⃣ Testing inline review comments...")
    try:
        # Get the latest commit
        commits = list(pr.get_commits())
        if not commits:
            print("   ⚠️  No commits in PR, skipping inline comments")
        else:
            latest_commit = commits[-1]
            print(f"   Latest commit: {latest_commit.sha[:7]}")

            # Try to add an inline comment
            try:
                # Note: This may fail if the file/line doesn't exist in the diff
                pr.create_review_comment(
                    body="⚠️ **SQL Injection**: This line uses unsafe string formatting for SQL queries. Use parameterized queries instead.",
                    commit=latest_commit,
                    path="app.py",
                    line=15
                )
                print(f"   ✅ Inline comment added")
            except Exception as e:
                print(f"   ⚠️  Inline comment failed (file may not be in diff): {e}")
    except Exception as e:
        print(f"   ⚠️  Could not add inline comments: {e}")

    print()
    print("=" * 70)
    print("✅ TEST COMPLETE!")
    print("=" * 70)
    print(f"🔗 View PR with comments: {pr.html_url}")
    print()

    return 0

if __name__ == "__main__":
    try:
        sys.exit(main())
    except KeyboardInterrupt:
        print("\n\n⚠️  Test interrupted")
        sys.exit(1)
    except Exception as e:
        print(f"\n\n❌ Error: {e}")
        import traceback
        traceback.print_exc()
        sys.exit(1)
