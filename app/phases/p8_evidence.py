"""Phase 8: Evidence Generation - Create detailed security evidence in GCS"""
from .base import Phase
from typing import Dict, Any, List
from google.cloud import storage
import os
import json
from datetime import datetime
from vertexai.generative_models import GenerativeModel

class Phase8Evidence(Phase):
    """Generate and upload security evidence to GCS"""

    def execute(self) -> Dict[str, Any]:
        self.logger.info("Phase 8: Generating security evidence")

        try:
            # Only generate evidence for PATCH mode (when we create PRs)
            scan_mode = self.context.get("scan_mode")
            self.logger.info(f"Detected scan_mode: {scan_mode}")

            if scan_mode != "patch":
                self.logger.info(f"Skipping evidence generation for {scan_mode} mode")
                return {"evidence_generated": False, "reason": f"{scan_mode}_mode"}

            # Prepare evidence structure
            repo_name = self.context.get("repo_name", "unknown")
            scan_id = self.context.get("scan_id")
            evidence_path = f"{repo_name}/scan-{scan_id}"

            # Generate evidence files
            evidence_files = {}
            evidence_files["00_SUMMARY.md"] = self._generate_summary()
            evidence_files.update(self._generate_findings())
            evidence_files.update(self._generate_attack_patterns())
            evidence_files.update(self._generate_scan_metadata())
            evidence_files.update(self._generate_patches())

            # Upload to GCS
            bucket_name = os.getenv("EVIDENCE_BUCKET", f"security-patch-evidence-{os.getenv('GCP_PROJECT_ID')}")
            gcs_urls = self._upload_to_gcs(bucket_name, evidence_path, evidence_files)

            evidence_url = f"gs://{bucket_name}/{evidence_path}/"

            # Add evidence link as PR comment
            self._add_evidence_comment_to_pr(evidence_url)

            result = {
                "evidence_generated": True,
                "evidence_path": evidence_url,
                "files_created": len(evidence_files),
                "gcs_urls": gcs_urls
            }

            self.update_context(result)
            self.logger.info(f"Evidence uploaded to {evidence_url}")

            return result

        except Exception as e:
            self.logger.error(f"Evidence generation failed: {e}")
            return {"evidence_generated": False, "error": str(e)}

    def _generate_summary(self) -> str:
        """Generate 00_SUMMARY.md"""
        scan_id = self.context.get("scan_id")
        repo_url = self.context.get("repo_url")
        repo_name = self.context.get("repo_name")
        vulnerabilities = self.context.get("vulnerabilities", [])
        patches = self.context.get("patches", [])
        pr_url = self.context.get("pr_url", "N/A")

        # Count by severity
        critical = sum(1 for v in vulnerabilities if v.get('severity') == 'CRITICAL')
        high = sum(1 for v in vulnerabilities if v.get('severity') == 'HIGH')
        medium = sum(1 for v in vulnerabilities if v.get('severity') in ['MEDIUM', 'WARNING'])
        low = sum(1 for v in vulnerabilities if v.get('severity') == 'LOW')

        # Calculate aggregate CVSS (highest score)
        max_cvss = 9.0 if critical > 0 else 7.0 if high > 0 else 5.0 if medium > 0 else 3.0

        summary = f"""# Security Scan Summary — {repo_name}

**Scan ID:** `{scan_id}`
**Repository:** {repo_url}
**Timestamp:** {datetime.utcnow().isoformat()}Z
**Status:** ✅ PATCHED

## Overview

**Aggregate CVSS Score:** **{max_cvss}** ({'CRITICAL' if critical > 0 else 'HIGH' if high > 0 else 'MEDIUM' if medium > 0 else 'LOW'})

### Vulnerabilities Detected
- 🔴 **Critical:** {critical}
- 🟠 **High:** {high}
- 🟡 **Medium:** {medium}
- 🟢 **Low:** {low}
- **Total:** {len(vulnerabilities)}

### Remediation
- **Patches Generated:** {len(patches)}
- **Files Modified:** {len(set(p.get('file_path') for p in patches))}
- **Pull Request:** {pr_url}

## Evidence Structure

```
{repo_name}/scan-{scan_id}/
├── 00_SUMMARY.md              (this file)
├── findings/
│   ├── F-01-*.md              (detailed findings with CVSS)
│   └── 00_INDEX.md
├── attack-patterns/
│   ├── P-01-*.md              (exploitation primitives)
│   └── 00_INDEX.md
├── scan-metadata/
│   ├── semgrep-output.json    (raw scan data)
│   ├── llm-analysis.json      (Gemini reasoning)
│   └── phase-timings.json     (performance metrics)
└── patches/
    ├── *.before               (original vulnerable code)
    ├── *.after                (patched code)
    └── *.diff                 (git diff format)
```

## Next Steps

1. Review findings in `findings/` directory
2. Understand attack vectors in `attack-patterns/`
3. Review the pull request: {pr_url}
4. Merge after validation

---

🤖 Generated by Security Patch Agent v1.0.0
"""
        return summary

    def _generate_findings(self) -> Dict[str, str]:
        """Generate findings/F-NN-{type}.md for each vulnerability"""
        vulnerabilities = self.context.get("vulnerabilities", [])
        findings = {}

        # Sort by severity (CRITICAL > HIGH > MEDIUM > LOW)
        severity_order = {'CRITICAL': 0, 'ERROR': 1, 'HIGH': 2, 'WARNING': 3, 'MEDIUM': 4, 'LOW': 5}
        sorted_vulns = sorted(
            vulnerabilities,
            key=lambda v: severity_order.get(v.get('severity', 'LOW'), 99)
        )

        # Generate index
        index_content = "# Findings Index\n\n"
        index_content += "| ID | Type | Severity | File | CVSS |\n"
        index_content += "|---|---|---|---|---|\n"

        # Only generate detailed LLM evidence for top 5, rest get simple template
        detailed_limit = 5
        total_to_document = min(len(sorted_vulns), 20)  # Cap at 20 total

        for idx, vuln in enumerate(sorted_vulns[:total_to_document], 1):
            vuln_type = vuln.get('type', 'Unknown').replace(' ', '-').lower()
            finding_file = f"findings/F-{idx:02d}-{vuln_type}.md"

            # Generate CVSS score
            cvss_vector, cvss_score = self._calculate_cvss(vuln)

            # Top 5: Full LLM-generated finding. Rest: Simple template
            if idx <= detailed_limit:
                self.logger.info(f"Generating detailed finding {idx}/{detailed_limit} (LLM)")
                finding_content = self._format_finding(idx, vuln, cvss_vector, cvss_score)
            else:
                self.logger.info(f"Generating simple finding {idx}/{total_to_document} (template)")
                finding_content = self._format_finding_fallback(idx, vuln, cvss_vector, cvss_score)

            findings[finding_file] = finding_content

            # Add to index
            file_name = vuln.get('file', 'N/A').split('/')[-1] if vuln.get('file') else 'N/A'
            index_content += f"| F-{idx:02d} | {vuln.get('type')} | {vuln.get('severity')} | {file_name} | {cvss_score} |\n"

        findings["findings/00_INDEX.md"] = index_content
        return findings

    def _format_finding(self, idx: int, vuln: Dict, cvss_vector: str, cvss_score: float) -> str:
        """Generate finding document using LLM for content"""
        try:
            model = GenerativeModel("gemini-2.5-pro")

            prompt = f"""You are a security expert. Create a detailed security finding document.

Vulnerability Data:
- Type: {vuln.get('type')}
- File: {vuln.get('file')}
- Line: {vuln.get('line')}
- Severity: {vuln.get('severity')}
- Description: {vuln.get('description')}
- Code Snippet: {vuln.get('code_snippet', 'N/A')}
- CVSS Vector: {cvss_vector}
- CVSS Score: {cvss_score}

You MUST respond with ONLY a valid JSON object in this exact format:
{{
  "summary": "One paragraph explaining what the vulnerability is, where it is, and the immediate impact",
  "root_cause": "Technical explanation of why this code is vulnerable",
  "attack_scenario": "Step-by-step how an attacker would exploit this",
  "impact": "What an attacker gains - be specific (data exfiltration, RCE, privilege escalation, etc)",
  "remediation": "Specific fix applied - technical details of the patch"
}}

Do not include markdown, explanations, or extra text. Output ONLY the JSON object."""

            response = model.generate_content(prompt)
            result_text = response.text.strip()

            # Remove markdown if present
            if result_text.startswith('```'):
                result_text = result_text.split('```')[1]
                if result_text.startswith('json'):
                    result_text = result_text[4:]
                result_text = result_text.strip()

            content = json.loads(result_text)

            # Build markdown from LLM content
            file_path = vuln.get('file', 'N/A')
            return f"""# F-{idx:02d} — {vuln.get('type')}

**Severity:** `{cvss_vector}` → **{cvss_score}**
**Status:** ✅ PATCHED
**Component:** `{file_path}:{vuln.get('line')}`

## Summary

{content['summary']}

## Root Cause

{content['root_cause']}

**File:** `{file_path}:{vuln.get('line')}`

**Vulnerable Code:**
```
{vuln.get('code_snippet', 'N/A')}
```

## Attack Scenario

{content['attack_scenario']}

## Impact

{content['impact']}

## Fix Applied

{content['remediation']}

The vulnerability has been automatically patched. See `patches/{os.path.basename(file_path)}.diff` for complete code changes.

---

🔬 Status: CONFIRMED → ✅ PATCHED
"""

        except Exception as e:
            self.logger.warning(f"LLM finding generation failed: {e}, using template")
            # Fallback to simple template
            return self._format_finding_fallback(idx, vuln, cvss_vector, cvss_score)

    def _format_finding_fallback(self, idx: int, vuln: Dict, cvss_vector: str, cvss_score: float) -> str:
        """Fallback template if LLM fails"""
        vuln_type = vuln.get('type', 'Unknown')
        file_path = vuln.get('file', 'N/A')

        return f"""# F-{idx:02d} — {vuln_type}

**Severity:** `{cvss_vector}` → **{cvss_score}**
**Status:** ✅ PATCHED
**Component:** `{file_path}:{vuln.get('line')}`

## Summary

{vuln.get('description', 'Security vulnerability detected')}

**File:** `{file_path}:{vuln.get('line')}`
**Code:** `{vuln.get('code_snippet', 'N/A')}`

---

🔬 Status: CONFIRMED → ✅ PATCHED
"""

    def _generate_attack_patterns(self) -> Dict[str, str]:
        """Generate attack-patterns/P-NN-{type}.md"""
        vulnerabilities = self.context.get("vulnerabilities", [])
        patterns = {}

        # Group vulnerabilities by type to create attack primitives
        vuln_types = {}
        for vuln in vulnerabilities:
            vtype = vuln.get('type', 'Unknown')
            if vtype not in vuln_types:
                vuln_types[vtype] = []
            vuln_types[vtype].append(vuln)

        # Sort by count (most common first) and limit to top 3 types for LLM generation
        sorted_types = sorted(vuln_types.items(), key=lambda x: len(x[1]), reverse=True)
        detailed_limit = 3

        # Generate index
        index_content = "# Attack Patterns Index\n\n"
        index_content += "Confirmed exploitation primitives.\n\n"
        index_content += "| ID | Pattern | Instances | Detail Level |\n"
        index_content += "|---|---|---|---|\n"

        for idx, (vtype, vulns) in enumerate(sorted_types[:10], 1):  # Cap at 10 types
            pattern_file = f"attack-patterns/P-{idx:02d}-{vtype.replace(' ', '-').lower()}.md"

            # Top 3: Full LLM-generated pattern. Rest: Simple template
            if idx <= detailed_limit:
                self.logger.info(f"Generating detailed attack pattern {idx}/{detailed_limit} (LLM)")
                pattern_content = self._format_attack_pattern(idx, vtype, vulns)
            else:
                self.logger.info(f"Generating simple attack pattern {idx}/10 (template)")
                pattern_content = self._format_attack_pattern_fallback(idx, vtype, vulns)

            patterns[pattern_file] = pattern_content

            detail_level = "Detailed (LLM)" if idx <= detailed_limit else "Summary"
            index_content += f"| P-{idx:02d} | {vtype} | {len(vulns)} | {detail_level} |\n"

        patterns["attack-patterns/00_INDEX.md"] = index_content
        return patterns

    def _format_attack_pattern(self, idx: int, vuln_type: str, vulns: List[Dict]) -> str:
        """Generate attack pattern document using LLM"""
        try:
            model = GenerativeModel("gemini-2.5-pro")
            first_vuln = vulns[0]

            prompt = f"""You are a security expert. Create an attack pattern / exploitation primitive document.

Vulnerability Type: {vuln_type}
Instances Found: {len(vulns)}
Example File: {first_vuln.get('file')}
Example Code: {first_vuln.get('code_snippet', 'N/A')}

You MUST respond with ONLY a valid JSON object in this exact format:
{{
  "what": "Concise description of the exploitation primitive (2-3 sentences)",
  "preconditions": "What an attacker needs to exploit this (access level, inputs, etc)",
  "gives_you": "What the attacker gains (RCE, data access, privilege escalation, etc)",
  "exploitation_steps": "Step-by-step attack walkthrough (numbered list as string)",
  "chains_with": "Other vulnerabilities this can chain with (array of strings)",
  "mitigation": "How this primitive was neutralized"
}}

Do not include markdown, explanations, or extra text. Output ONLY the JSON object."""

            response = model.generate_content(prompt)
            result_text = response.text.strip()

            # Remove markdown if present
            if result_text.startswith('```'):
                result_text = result_text.split('```')[1]
                if result_text.startswith('json'):
                    result_text = result_text[4:]
                result_text = result_text.strip()

            content = json.loads(result_text)

            chains_list = '\n'.join([f"- {c}" for c in content.get('chains_with', [])])

            return f"""# P-{idx:02d} — {vuln_type} Exploitation Primitive

**Type:** {vuln_type}
**Instances Found:** {len(vulns)}
**Status:** ✅ PATCHED

## What

{content['what']}

## Precondition

{content['preconditions']}

## Gives You

{content['gives_you']}

## Proof

**File:** `{first_vuln.get('file', 'N/A')}:{first_vuln.get('line', 0)}`

**Vulnerable Pattern:**
```
{first_vuln.get('code_snippet', 'N/A')}
```

## Exploitation Steps

{content['exploitation_steps']}

## Chains With

{chains_list}

## Mitigation

{content['mitigation']}

---

🔬 Status: CONFIRMED → ✅ PATCHED
"""

        except Exception as e:
            self.logger.warning(f"LLM attack pattern generation failed: {e}, using template")
            return self._format_attack_pattern_fallback(idx, vuln_type, vulns)

